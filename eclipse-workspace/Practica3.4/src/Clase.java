
public class Clase {
	
	private String modelo = "", ruedas = "";
	private Integer platos, pinones;
	private Float precio;
	
	public Clase (String modelo, String ruedas, Integer platos, Integer pinones, Float precio){
		this.modelo = modelo;
		this.ruedas = ruedas;
		this.platos = platos;
		this.pinones = pinones;
		this.precio = precio;
	}
	
}
